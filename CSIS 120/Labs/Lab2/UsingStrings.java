public class UsingStrings
{
    public static void main (String[] args)
    {
        // CREATES A NEW STRING OBJECT
        String originalString = "This Is A Test";
        
        // PRINTS STRING OBJECT originalString
        System.out.println("Original string: " + originalString);
        System.out.println("");
        
        //CREATES A NEW STRING OBJECT
        String smallString = originalString.toLowerCase();
        
        // PRINTS STRING OBJECT smallString
        System.out.println("Small string: " + smallString);
        System.out.println("");
        
        // CREATES A NEW STRING OBJECT
        String bigString = originalString.toUpperCase();
        
        // PRINTS STRING OBJECT bigString
        System.out.println("Big string: " + bigString);
        System.out.println("");
        
        
        
        
        
        
        
        
        
    }
}
